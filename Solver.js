var getPixelMapFromImageData = function (imgData) {
  var pixMap = [];
  var pixMap2 = []
  var count = 0;
  var temp = [];
  var L;

  for (var i = 0; i < imgData.data.length; i += 4) {
    L = (imgData.data[i + 0] * 299 + imgData.data[i + 1] * 587 + imgData.data[i + 2] * 114) / 1000;
    if (L >= 30)
      temp.push(0);
    else
      temp.push(1);
    count++;
    if (count % 180 == 0) {
      pixMap.push(temp);
      temp = [];
    }
  }
  return pixMap;
};


function getCaptcha(captcha) {
  move = 0
  prev_width = 0
  res = ""
  ch = null
  for (var i = 0; i < 6; i++) {
    if (res.length == 6) {
      break;
    }
    else {
      for (var l = 0; l < 34; l++) {
        char = order[l];
        toMatch = keys[char];
        height = toMatch.length
        width = toMatch[0].length
        for (var m = 0; m < 20; m++) {
          totalMatch = 0.0
          properMatch = 0.0

          for (var x = 0; x < height; x++) {
            for (var y = 0; y < width; y++) {
              if (toMatch[x][y] == 1 && y + move + m < 180) {
                totalMatch += 1
                if (captcha[x + offSet[char]][y + move + m] == 1)
                  properMatch += 1
              }
            }
          }

          if ((l == 15 || l == 0) && properMatch / totalMatch > 0.85) {
            res += char;
            prev_width = width;
            move += prev_width + m;
            l = 35;
            break;
          }
          if (properMatch / totalMatch > 0.93) {
            res += char;
            prev_width = width;
            move += prev_width + m;
            l = 35;
            break;
          }
        }
      }
    }
  }
  return res
}


